package basics;

public class DataTypes {

    public static void main(String[] args) {

        //byte example - if greater than 128 it's not gonna printout
        byte dogAge = 13;
        System.out.println();


        //short example
        //max value 32,767
        short myShort = 8325;
        System.out.println();

        int accountBalance = 5345244;
        System.out.println();


        long usDebt = 4342345234L;
        System.out.println();


        double average = 44.33;
        System.out.println();


        float anotherAverage = 4.7f;
        System.out.println();


        char letterGrade = 'A';
        System.out.println();

        //just one letter for char, and single quotations (')

        boolean isDoorOpen = false;
        System.out.println();

        //the reason we are using "is" is because we are kinda of asking a question
        //if user opens door




        //two datatypes we should take from this is INT and DOUBLE
        //Int for numbers
        //Double for decimal point values


    }
}
