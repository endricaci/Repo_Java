package class_2020;

public class classWork {

    public static void main(String[] args) {

        /**
         * increment (++)
         * post-increment and pre-increment
         * b++ post-increment
         * ++b pre-increment
         */

        int inc = 10;
        inc++;
        System.out.println(inc);
        ++inc;
        System.out.println(inc);


        int i1 = 1;
        int i2 = i1++;

        System.out.println(i1); //
        System.out.println(i2); //

    }


}
